function renderProducts() {
    const container = document.getElementById("product-container");
    container.innerHTML = "";

    products.forEach(product => {
        const card = document.createElement("div");
        card.classList.add("card");

        card.innerHTML = `
            <div class="product-number">${product.number}</div>
            <a href="${product.link}" target="_blank">
                <img src="${product.image}" alt="${product.name}">
            </a>
            <div class="product-name">${product.name}</div>
            <a href="${product.link}" target="_blank">
                <button class="buy-btn">Buy Now</button>
            </a>
        `;

        container.appendChild(card);
    });
}

function filterProducts() {
    const searchValue = document.getElementById("search").value.toLowerCase().replace("#", "");

    document.querySelectorAll(".card").forEach((card, index) => {
        const product = products[index];
        const productName = product.name.toLowerCase();
        const productNumber = product.number.toLowerCase().replace("#", "");

        const matches = productName.includes(searchValue) || productNumber.includes(searchValue);
        card.style.display = matches ? "block" : "none";
    });
}

document.addEventListener("DOMContentLoaded", renderProducts);
